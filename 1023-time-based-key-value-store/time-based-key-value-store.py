class TimeMap:

    def __init__(self):
        self.map = defaultdict(list)
        

    def set(self, key: str, value: str, timestamp: int) -> None:
        self.map[key].append((timestamp, value))
        

    def get(self, key: str, timestamp: int) -> str:
        values = self.map.get(key)
        if not values:
            return ""
        
        for t, v in reversed(values):
            if t <= timestamp:
                return v
        return ""


